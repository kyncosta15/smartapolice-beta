import { useState, useEffect } from 'react';
import { ParsedPolicyData } from '@/utils/policyDataParser';
import { PolicyPersistenceService } from '@/services/policyPersistenceService';
import { useAuth } from '@/contexts/AuthContext';
import { useToast } from '@/hooks/use-toast';
import { supabase } from '@/integrations/supabase/client';

export function usePersistedPolicies() {
  const [policies, setPolicies] = useState<ParsedPolicyData[]>([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const { user } = useAuth();
  const { toast } = useToast();

  // Mapeamento de status para compatibilidade com dados antigos
  const mapLegacyStatus = (status: string) => {
    switch (status) {
      case 'active':
        return 'vigente';
      case 'expiring':
        return 'renovada_aguardando';
      case 'expired':
        return 'nao_renovada';
      default:
        return status;
    }
  };

  // Carregar ap√≥lices quando usu√°rio faz login
  useEffect(() => {
    console.log(`üîç [usePersistedPolicies] Verificando estado do usu√°rio:`, {
      userId: user?.id,
      userExists: !!user,
      timestamp: new Date().toISOString()
    });
    
    if (user?.id) {
      console.log(`üîÑ [usePersistedPolicies] Usu√°rio logado detectado - Iniciando carregamento de ap√≥lices para: ${user.id}`);
      loadPersistedPolicies();
    } else {
      console.log(`üö™ [usePersistedPolicies] Usu√°rio n√£o logado - Limpando dados das ap√≥lices`);
      // Limpar dados quando usu√°rio faz logout
      setPolicies([]);
    }
  }, [user?.id]);

  const loadPersistedPolicies = async () => {
    if (!user?.id) {
      console.log(`‚ö†Ô∏è [loadPersistedPolicies] Chamado sem userId v√°lido`);
      return;
    }

    console.log(`üöÄ [loadPersistedPolicies] Iniciando para userId: ${user.id} √†s ${new Date().toISOString()}`);
    setIsLoading(true);
    setError(null);

    try {
      console.log(`üîÑ [loadPersistedPolicies] Carregando ap√≥lices persistidas do usu√°rio: ${user.id}`);
      
      // Primeiro, limpar duplicatas se existirem
      const cleanedCount = await PolicyPersistenceService.cleanupDuplicatePolicies(user.id);
      if (cleanedCount > 0) {
        console.log(`üßπ [loadPersistedPolicies] ${cleanedCount} ap√≥lices duplicadas foram removidas`);
        toast({
          title: "üßπ Limpeza Realizada",
          description: `${cleanedCount} ap√≥lices duplicadas foram removidas`,
        });
      }
      
      const loadedPolicies = await PolicyPersistenceService.loadUserPolicies(user.id);
      
      console.log(`üîç [loadPersistedPolicies] Resultado do PolicyPersistenceService.loadUserPolicies:`, {
        length: loadedPolicies.length,
        policyIds: loadedPolicies.map(p => ({ id: p.id, name: p.name })),
        timestamp: new Date().toISOString()
      });
      
      // Mapear status para novos valores
      const mappedPolicies = loadedPolicies.map(policy => ({
        ...policy,
        status: mapLegacyStatus(policy.status)
      }));
      
      console.log(`üìä [loadPersistedPolicies] Definindo ${mappedPolicies.length} ap√≥lices no estado local`);
      setPolicies(mappedPolicies);
      
      if (mappedPolicies.length > 0) {
        console.log(`‚úÖ [loadPersistedPolicies] ${mappedPolicies.length} ap√≥lices carregadas com sucesso`);
        console.log(`üìö [loadPersistedPolicies] IDs das ap√≥lices carregadas:`, mappedPolicies.map(p => p.id));
      } else {
        console.log('üì≠ [loadPersistedPolicies] Nenhuma ap√≥lice encontrada no hist√≥rico');
      }

    } catch (err) {
      const errorMessage = err instanceof Error ? err.message : 'Erro ao carregar dados';
      setError(errorMessage);
      console.error('‚ùå [loadPersistedPolicies] Erro ao carregar ap√≥lices persistidas:', err);
      
      toast({
        title: "‚ùå Erro ao Carregar Dados",
        description: errorMessage,
        variant: "destructive",
      });
    } finally {
      setIsLoading(false);
      console.log(`üèÅ [loadPersistedPolicies] Finalizado para userId: ${user.id} √†s ${new Date().toISOString()}`);
    }
  };

  // Adicionar nova ap√≥lice √† lista
  const addPolicy = (policy: ParsedPolicyData) => {
    console.log(`‚ûï [addPolicy] Adicionando ap√≥lice ao estado local:`, {
      policyId: policy.id,
      policyName: policy.name,
      currentPoliciesCount: policies.length,
      timestamp: new Date().toISOString()
    });
    
    const mappedPolicy = {
      ...policy,
      status: mapLegacyStatus(policy.status)
    };
    
    setPolicies(prev => {
      const newPolicies = [mappedPolicy, ...prev];
      console.log(`üìä [addPolicy] Estado atualizado - Total de ap√≥lices: ${newPolicies.length}`);
      return newPolicies;
    });
  };

  // Remover ap√≥lice da lista
  const removePolicy = (policyId: string) => {
    console.log(`‚ûñ [removePolicy] Removendo ap√≥lice do estado local:`, {
      policyId,
      currentPoliciesCount: policies.length,
      timestamp: new Date().toISOString()
    });
    
    setPolicies(prev => {
      const newPolicies = prev.filter(p => p.id !== policyId);
      console.log(`üìä [removePolicy] Estado atualizado - Total de ap√≥lices: ${newPolicies.length}`);
      console.log(`üìä [removePolicy] IDs restantes:`, newPolicies.map(p => p.id));
      return newPolicies;
    });
  };

  // Deletar ap√≥lice do banco de dados
  const deletePolicy = async (policyId: string): Promise<boolean> => {
    console.log(`üóëÔ∏è [deletePolicy] INICIANDO dele√ß√£o da ap√≥lice: ${policyId} √†s ${new Date().toISOString()}`);
    
    if (!user?.id) {
      console.log(`‚ùå [deletePolicy] Usu√°rio n√£o autenticado para deletar ap√≥lice: ${policyId}`);
      toast({
        title: "‚ùå Erro de Autentica√ß√£o",
        description: "Usu√°rio n√£o autenticado",
        variant: "destructive",
      });
      return false;
    }

    // Verificar se a ap√≥lice existe no estado local antes de deletar
    const policyExists = policies.find(p => p.id === policyId);
    console.log(`üîç [deletePolicy] Ap√≥lice existe no estado local:`, {
      exists: !!policyExists,
      policyName: policyExists?.name,
      currentPoliciesCount: policies.length
    });

    try {
      console.log(`üîÑ [deletePolicy] Iniciando dele√ß√£o no banco para ap√≥lice: ${policyId}`);
      
      // Obter token de autentica√ß√£o atual
      const { data: { session } } = await supabase.auth.getSession();
      
      if (!session) {
        throw new Error("Sess√£o de usu√°rio inv√°lida");
      }
      
      console.log(`üì° [deletePolicy] Chamando Edge Function para deletar ap√≥lice: ${policyId}`);
      
      // Chamar a Edge Function para deletar a ap√≥lice e todos os dados relacionados
      const response = await fetch(`https://jhvbfvqhuemuvwgqpskz.supabase.co/functions/v1/delete-policy`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${session.access_token}`
        },
        body: JSON.stringify({ policyId })
      });
      
      const result = await response.json();
      console.log(`üì° [deletePolicy] Resposta da Edge Function:`, {
        status: response.status,
        result,
        policyId
      });
      
      if (!response.ok) {
        throw new Error(result.error || 'Erro ao deletar ap√≥lice');
      }

      console.log(`‚úÖ [deletePolicy] Ap√≥lice deletada com sucesso no banco: ${policyId}`);
      
      // Remover do estado local AP√ìS confirma√ß√£o do banco
      console.log(`üìä [deletePolicy] Removendo do estado local ap√≥s confirma√ß√£o do banco`);
      removePolicy(policyId);
      
      toast({
        title: "‚úÖ Ap√≥lice Deletada",
        description: "A ap√≥lice foi removida com sucesso",
      });
      
      // Verificar estado ap√≥s dele√ß√£o
      console.log(`üîç [deletePolicy] Estado ap√≥s dele√ß√£o:`, {
        remainingPoliciesCount: policies.length - 1, // -1 porque removePolicy ainda n√£o executou
        deletedPolicyId: policyId,
        timestamp: new Date().toISOString()
      });
      
      return true;
    } catch (error) {
      console.error('‚ùå [deletePolicy] ERRO CR√çTICO ao deletar ap√≥lice:', {
        policyId,
        error: error instanceof Error ? error.message : error,
        stack: error instanceof Error ? error.stack : undefined,
        timestamp: new Date().toISOString()
      });
      
      toast({
        title: "‚ùå Erro ao Deletar",
        description: error instanceof Error ? error.message : "N√£o foi poss√≠vel remover a ap√≥lice",
        variant: "destructive",
      });
      return false;
    }
  };

  // Atualizar ap√≥lice no banco de dados
  const updatePolicy = async (policyId: string, updates: Partial<ParsedPolicyData>): Promise<boolean> => {
    if (!user?.id) {
      toast({
        title: "‚ùå Erro de Autentica√ß√£o",
        description: "Usu√°rio n√£o autenticado",
        variant: "destructive",
      });
      return false;
    }

    try {
      console.log(`‚úèÔ∏è Atualizando ap√≥lice: ${policyId}`, updates);
      
      // Converter dados para formato do banco - mapeando TODOS os campos edit√°veis
      const dbUpdates: any = {};
      
      // Campos b√°sicos
      if (updates.name !== undefined) dbUpdates.segurado = updates.name;
      if (updates.insurer !== undefined) dbUpdates.seguradora = updates.insurer;
      if (updates.type !== undefined) dbUpdates.tipo_seguro = updates.type;
      if (updates.policyNumber !== undefined) dbUpdates.numero_apolice = updates.policyNumber;
      if (updates.premium !== undefined) dbUpdates.valor_premio = updates.premium;
      if (updates.monthlyAmount !== undefined) dbUpdates.custo_mensal = updates.monthlyAmount;
      if (updates.startDate !== undefined) dbUpdates.inicio_vigencia = updates.startDate;
      if (updates.endDate !== undefined) dbUpdates.fim_vigencia = updates.endDate;
      if (updates.status !== undefined) dbUpdates.status = mapLegacyStatus(updates.status);
      if (updates.category !== undefined) dbUpdates.forma_pagamento = updates.category;
      if (updates.entity !== undefined) dbUpdates.corretora = updates.entity;
      
      // Campos espec√≠ficos do N8N
      if (updates.insuredName !== undefined) dbUpdates.segurado = updates.insuredName; // Priorizar insuredName sobre name
      if (updates.documento !== undefined) dbUpdates.documento = updates.documento;
      if (updates.documento_tipo !== undefined) dbUpdates.documento_tipo = updates.documento_tipo;
      if (updates.vehicleModel !== undefined) dbUpdates.modelo_veiculo = updates.vehicleModel;
      if (updates.uf !== undefined) dbUpdates.uf = updates.uf;
      if (updates.deductible !== undefined) dbUpdates.franquia = updates.deductible;
      
      // Coverage - se for array, converter para string separada por v√≠rgula
      if (updates.coverage !== undefined) {
        const coverageString = Array.isArray(updates.coverage) 
          ? updates.coverage.join(', ') 
          : updates.coverage;
        // N√£o h√° campo espec√≠fico na DB para coverage, pode adicionar se necess√°rio
      }

      console.log('üìù Dados preparados para atualiza√ß√£o:', dbUpdates);

      const { error } = await supabase
        .from('policies')
        .update(dbUpdates)
        .eq('id', policyId)
        .eq('user_id', user.id); // Garantir que s√≥ edite as pr√≥prias

      if (error) {
        throw error;
      }

      // Atualizar estado local com mapeamento de status
      const mappedUpdates = {
        ...updates,
        status: updates.status ? mapLegacyStatus(updates.status) : undefined
      };
      
      setPolicies(prev => 
        prev.map(p => p.id === policyId ? { ...p, ...mappedUpdates } : p)
      );
      
      toast({
        title: "‚úÖ Ap√≥lice Atualizada",
        description: "As altera√ß√µes foram salvas com sucesso",
      });
      
      return true;
    } catch (error) {
      console.error('‚ùå Erro ao atualizar ap√≥lice:', error);
      toast({
        title: "‚ùå Erro ao Atualizar",
        description: "N√£o foi poss√≠vel salvar as altera√ß√µes",
        variant: "destructive",
      });
      return false;
    }
  };

  // Obter URL de download para um PDF
  const getPDFDownloadUrl = async (policyId: string): Promise<string | null> => {
    const policy = policies.find(p => p.id === policyId);
    
    console.log(`üîç Tentativa de download - Policy ID: ${policyId}`);
    console.log(`üîç Policy encontrada:`, policy);
    console.log(`üîç PDF Path da policy: ${policy?.pdfPath}`);
    
    if (!policy?.pdfPath) {
      console.log(`‚ùå Policy sem pdfPath: ${policy?.name}`);
      toast({
        title: "‚ùå Arquivo n√£o encontrado",
        description: "PDF n√£o est√° dispon√≠vel para download",
        variant: "destructive",
      });
      return null;
    }

    try {
      console.log(`üì• Solicitando URL de download para: ${policy.pdfPath}`);
      const downloadUrl = await PolicyPersistenceService.getPDFDownloadUrl(policy.pdfPath);
      
      if (!downloadUrl) {
        console.log(`‚ùå URL de download n√£o gerada para: ${policy.pdfPath}`);
        toast({
          title: "‚ùå Erro no Download",
          description: "N√£o foi poss√≠vel gerar o link de download",
          variant: "destructive",
        });
        return null;
      }

      console.log(`‚úÖ URL de download gerada: ${downloadUrl}`);
      return downloadUrl;
    } catch (error) {
      console.error('‚ùå Erro ao obter URL de download:', error);
      toast({
        title: "‚ùå Erro no Download",
        description: "Falha ao acessar o arquivo PDF",
        variant: "destructive",
      });
      return null;
    }
  };

  // Baixar PDF de uma ap√≥lice
  const downloadPDF = async (policyId: string, policyName: string) => {
    const downloadUrl = await getPDFDownloadUrl(policyId);
    
    if (downloadUrl) {
      // Criar link tempor√°rio para download
      const link = document.createElement('a');
      link.href = downloadUrl;
      link.download = `${policyName}.pdf`;
      document.body.appendChild(link);
      link.click();
      document.body.removeChild(link);
      
      toast({
        title: "üì• Download Iniciado",
        description: `Baixando arquivo: ${policyName}.pdf`,
      });
    }
  };

  // Recarregar dados
  const refreshPolicies = () => {
    console.log(`üîÑ [refreshPolicies] Solicitado recarregamento manual √†s ${new Date().toISOString()}`);
    if (user?.id) {
      console.log(`üîÑ [refreshPolicies] Chamando loadPersistedPolicies para userId: ${user.id}`);
      loadPersistedPolicies();
    } else {
      console.log(`‚ö†Ô∏è [refreshPolicies] Usu√°rio n√£o logado - n√£o √© poss√≠vel recarregar`);
    }
  };

  // Log do estado atual sempre que policies mudar
  useEffect(() => {
    console.log(`üìä [usePersistedPolicies] Estado atual das ap√≥lices:`, {
      count: policies.length,
      policyIds: policies.map(p => ({ id: p.id, name: p.name })),
      userId: user?.id,
      timestamp: new Date().toISOString()
    });
  }, [policies, user?.id]);

  return {
    policies,
    isLoading,
    error,
    addPolicy,
    removePolicy,
    deletePolicy,
    updatePolicy,
    downloadPDF,
    refreshPolicies,
    hasPersistedData: policies.length > 0
  };
}
